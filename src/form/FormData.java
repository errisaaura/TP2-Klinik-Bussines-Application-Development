    private void CariData(){
        try{
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM dt_pasien WHERE " + cmbCari.getSelectedItem().toString() + " LIKE '%" + txtCari.getText() + "%' ");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No.");
            model.addColumn("NIK");
            model.addColumn("nama");
            model.addColumn("tgl_lahir");
            model.addColumn("alamat");
               
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            int no = 1;
            while(rs.next()){
                Object[] data = {
                    no++,
                    rs.getString("NIK"),
                    rs.getString("nama"),
                    new SimpleDateFormat("yyyy-MMM-dd").format(rs.getDate("tgl_lahir")),
                    rs.getString("alamat"),
                };
                model.addRow(data);
                tblData.setModel(model);
            }
                     
        }catch (Exception e){
             
        }
    }
    
