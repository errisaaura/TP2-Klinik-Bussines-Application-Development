    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try{
            st = cn.createStatement();
            if (txtNIK.getText().equals("") || txtNama.getText().equals("") || inputTglLahir.getDate() == null || txtAlm.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Data tidak boleng kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            if (txtNIK.getText().length() > 15) {
                JOptionPane.showMessageDialog(this, "NIK Tidak Boleh lebih dari 15 digit , silahkan input ulang!", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            if (txtNama.getText().length() > 20) {
                JOptionPane.showMessageDialog(this, "Nama Tidak Boleh lebih dari 20 huruf , silahkan input ulang!", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            if (txtAlm.getText().length() > 50) {
                JOptionPane.showMessageDialog(this, "Alamat Tidak Boleh lebih dari 50 huruf , silahkan input ulang!", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
           
            if(btnSimpan.getText() == "Simpan"){
                //simpan data
                String cekNIK = "SELECT * FROM dt_pasien WHERE NIK = '" + txtNIK.getText() + "'";
                rs = st.executeQuery(cekNIK);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "[ERROR] NIK sudah terdaftar");
                }else{
                    String tglLahir = new SimpleDateFormat("yyyy-MM-dd").format(inputTglLahir.getDate());
                    String querySimpan = "INSERT INTO dt_pasien VALUES ('" + txtNIK.getText() +
                            "', '" + txtNama.getText() +
                            "', '" + tglLahir +
                            "','" + txtAlm.getText() + "')";
                    st.executeUpdate(querySimpan);
                    JOptionPane.showMessageDialog(null, "Data Pasien Berhasil Disimpan");
                    Bersih();
                    TampilData();
                }
            }else{
                // update data
                String tglLahir = new SimpleDateFormat("yyyy-MM-dd").format(inputTglLahir.getDate());
                String queryUpdate = "UPDATE dt_pasien SET nama = '" + txtNama.getText() + "', tgl_lahir = '" + tglLahir + "', alamat= '" + txtAlm.getText() + "' WHERE NIK = '" + txtNIK.getText() + "' ";
                st.executeUpdate(queryUpdate);
                JOptionPane.showMessageDialog(null, "Data Pasien Berhasil Diubah");
                Bersih();
                TampilData();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        // TODO add your handling code here:
        txtNIK.setText(tblData.getValueAt(tblData.getSelectedRow(), 1).toString());
        txtNama.setText(tblData.getValueAt(tblData.getSelectedRow(), 2).toString());
        txtAlm.setText(tblData.getValueAt(tblData.getSelectedRow(), 4).toString());
       
        String dateString = (String) tblData.getValueAt(tblData.getSelectedRow(), 3);
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MMM-dd");
        try {
            java.util.Date utilDate = format.parse(dateString);
            Date sqlDate = new Date(utilDate.getTime());
            inputTglLahir.setDate(sqlDate);
        } catch (ParseException e) {
            e.printStackTrace();
        }
        
        txtNIK.setEditable(false);
        btnSimpan.setText("Edit");
    }//GEN-LAST:event_tblDataMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if (txtNIK.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Silahkan pilih data pasien yang akan dihapus !");
        }else{
            int jawab = JOptionPane.showConfirmDialog(null, "Apakah data ini akan dihapus ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (jawab == 0){ //0 artinya yes
                try{
                    st = cn.createStatement();
                    String queryHapus = "DELETE FROM dt_pasien WHERE NIK='" + txtNIK.getText() + "'";
                    st.executeUpdate(queryHapus);
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                    TampilData();
                    Bersih();
                    
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

